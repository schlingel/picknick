    <h2>Härtung von Systemen</h2>
    <p>
Software wird nicht immer voll ausgetestet sondern nur einem Funktionstest unterzogen. Dadurch kann es zu Programmstellen kommen, die nicht bekannt sind und zu unvorhergesehenen Seiteneffekten führen.
Was bedeutet Härten?
BSI definiert es als: „Entfernung aller Softwarebestandteile die zur Vollführung der Aufgabe des Programmes nicht zwingend notwendig sind.“

